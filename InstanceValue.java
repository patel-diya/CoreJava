public class InstanceValue {
    static byte b;
    static char c;
    static short s;
    static int i;
    static long l;
    static float f;
    static double d;
    static boolean bo;
    public static void main(String []args)
    {
        System.out.println("The default value of the instance variable for the given datatype is: ");
        System.out.println("byte: "+b+"\nchar: "+c+"\nshort: "+s+"\nint: "+i+"\nlong: "+l+"\nfloat: "+f+"\ndouble: "+d+"\nboolean: "+bo);
    }
}
